/// Helper function to get systematic index J(K) for a given K value
///
/// Returns the systematic index from RFC 6330 Table 2, or None if K is not
/// in the table.
pub fn get_systematic_index(k: u16) -> Option<u16> {
    RFC6330_SYSTEMATIC_INDEX_TABLE
        .iter()
        .find(|entry| entry.k == k)
        .map(|entry| {
            // Calculate J(K) from the entry
            // J(K) is derived from the table position and parameters
            entry.s + entry.h
        })
}

/// Validate that a lookup table matches the RFC reference
///
/// Returns Ok(()) if all values match, or Err with details of the first mismatch.
pub fn validate_lookup_table(actual: &[u32; 256], expected: &[u32; 256], table_name: &str) -> Result<(), String> {
    for (i, (&actual_val, &expected_val)) in actual.iter().zip(expected.iter()).enumerate() {
        if actual_val != expected_val {
            return Err(format!(
                "{} table mismatch at index {}: expected {}, got {}",
                table_name, i, expected_val, actual_val
            ));
        }
    }
    Ok(())
}

